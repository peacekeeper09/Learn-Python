#Arithmetic Operators
#addition
x = 7
y = 9
print(x+y)
#output => 16

#subtraction
print(x-y)
#output => -2

#multiplication
print(x*y)
#output => 63

#division
print(x/y)
#output => 0.7777

#modulus
print(x%y)
#output => 7

#exponentiation	
print(x**y)
#output => 40353607

#floor division
print(x//y)
#output => 0

